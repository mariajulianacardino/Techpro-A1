public class SeatWorkNine {
   public static void main(String[] args){
        int[] numbers = {10, 20, 30, 40, 50};
        int index = 0;

        while( index < 5) {
            System.out.println("Element at index " + index + ": " + numbers[index]);
            index++;
            System.out.println("\n");
        }
   }
}
